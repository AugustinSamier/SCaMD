VOWELS = ["A", "E", "I", "O", "U"]


from comptra.languages import MAPPING_LANG_TO_KEY
from datasets import load_dataset
import json
import os

DATA_PATH = os.path.join(os.path.dirname(__file__), "..", "data/maps")


def get_keywords(src: str):
    """
    Get example keywords in the source language.
    - src: str,
        Source language e.g. English.
    """
    if src in LANG_TO_CODE:
        return KEYWORDS[LANG_TO_CODE[src]]
    filename = f"{MAPPING_LANG_TO_KEY[src]}.jsonl"
    keywords = []
    with open(os.path.join(DATA_PATH, filename), "r") as fin:
        for line in fin:
            keywords.append(json.loads(line)["keywords"])
    return keywords


def get_trigger_sents(src: str = "English"):
    """
    Get the trigger sentences in a specific language of interest.
    TRIGGER_SENTS contains the trigger sentences for 5 languages: English, French, Germman, japanese and Chinese.
    These sentences belong to the FLORES 200 dev set and they have the following indices: 0, 2, 5, 7 and 147.
    Arguments
    ---------
    - src: str,
        Source language e.g English.
    """
    if src in LANG_TO_CODE:
        return TRIGGER_SENTS[LANG_TO_CODE[src]]
    ds_src = load_dataset("facebook/flores", MAPPING_LANG_TO_KEY[src])["dev"].select(
        [0, 2, 5, 7, 147]
    )
    return [example["sentence"] for example in ds_src]


def get_demo_sents(src: str = "English", tgt: str = "Swahili"):
    """
    Get 5 (src, tgt) couple of sentences that are related to TRIGGER_SENTS.
    TRIGGER_SENTS consists of 5 sentences, which belong to the FLORES 200 dev set
    and have the following indices: 0, 2, 5, 7 and 147.

    For each sentence, we should provide a related one. This is simply done by
    considering the sentences in FLORES 200 dev set with the following indices:
    1, 3, 6, 8, 145.
    Arguments
    ---------
        - src: str,
            Source language e.g. English.
        - tgt: str,
            Target language e.g. Swahili.
    """
    if src in LANG_TO_CODE:
        src_sents = DEMO_SENTS[LANG_TO_CODE[src]]
    else:
        ds_src = load_dataset("facebook/flores", MAPPING_LANG_TO_KEY[src])[
            "dev"
        ].select([1, 3, 6, 8, 145])
        src_sents = [example["sentence"] for example in ds_src]
    if tgt in LANG_TO_CODE:
        tgt_sents = DEMO_SENTS[LANG_TO_CODE[tgt]]
    else:
        ds_tgt = load_dataset("facebook/flores", MAPPING_LANG_TO_KEY[tgt])[
            "dev"
        ].select([1, 3, 6, 8, 145])
        tgt_sents = [example["sentence"] for example in ds_tgt]

    return src_sents, tgt_sents


def get_maps_aspects(
    sentence: str,
    src: str,
    tgt: str,
    description: str,
    demos: str = None,
    keywords: str = None,
    topics: str = None,
):
    if description == "demos":
        left, right = get_demo_sents(src, tgt)
        sents = get_trigger_sents(src)
        prompt = ""
        for i, (a, b) in enumerate(zip(left, right)):
            article = "an" if any([src.startswith(vowel) for vowel in VOWELS]) else "a"
            prompt += f"Let's write {article} {src} sentence related to but different from the input {src} sentence and translate it into {tgt}\nInput {src} sentence: {sents[i]}\nOutput {src}-{tgt} sentence pair: {a}\t{b}"
            prompt += "\n\n"
        prompt += f"Let's write an {src} sentence related to but different from the input {src} sentence and translate it into {tgt}\nInput {src} sentence: {sentence}\nOutput {src}-{tgt} sentence pair:"
    elif description == "keywords":
        left = get_keywords(src)
        right = get_keywords(tgt)
        sents = get_trigger_sents(src)
        prompt = ""
        for i, (a, b) in enumerate(zip(left, right)):
            prompt += f"Let's extract the keywords in the following {src} sentence, and then translate these keywords into {tgt}.\n{src}: {sents[i]}\nKeyword Pairs: {', '.join([f'{a_}={b_}' for (a_, b_) in zip(a, b)])}"
            prompt += "\n\n"
        prompt += f"Let's extract the keywords in the following {src} sentence, and then translate these keywords into {tgt}.\n{src}: {sentence}\nKeyword Pairs:"
    elif description == "topics":
        left = TOPICS
        sents = get_trigger_sents(src)
        prompt = ""
        for i in range(len(sents)):
            prompt += f"Use a few words to describe the topics of the following input sentence.\nInput: {sents[i]}\nTopics: {left[i]}"
            prompt += "\n\n"
        prompt += f"Use a few words to describe the topics of the following input sentence.\nInput: {sentence}\nTopics:"
    elif description == "trans-zs":
        prompt = f"Instruction: Translate the following {src} text into {tgt}.\n{src}: {sentence}\n{tgt}:"
    elif description == "trans-demos":
        prompt = f"Related {src}-{tgt} sentence pairs: {demos}\n\nInstruction: Given the above knowledge, translate the following {src} text into {tgt}.\n{src}: {sentence}\n{tgt}:"
    elif description == "trans-keywords":
        prompt = f"Keyword Pairs: {keywords}\n\nInstruction: Given the above knowledge, translate the following {src} text into {tgt}.\n{src}: {sentence}\n{tgt}:"
    elif description == "trans-topics":
        prompt = f"Topics: {topics}\n\nInstruction: Given the above knowledge, translate the following {src} text into {tgt}.\n{src}: {sentence}\n{tgt}:"
    else:
        raise KeyError(f"The key {description} is not supported in MAPS.")
    return prompt


